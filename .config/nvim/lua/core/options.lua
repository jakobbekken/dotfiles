local opt = vim.opt

-- Line numbers
opt.number = true
opt.relativenumber = true

-- Tab and indentation
opt.tabstop = 2
opt.shiftwidth = 2
opt.expandtab = true
opt.autoindent = true
opt.smartindent = true

-- Line wrapping
opt.wrap = true

-- Scrolling and cursor
opt.scrolloff = 5
opt.cursorline = true

-- Appearance
opt.signcolumn = "no"
opt.splitright = true
opt.splitbelow = true
